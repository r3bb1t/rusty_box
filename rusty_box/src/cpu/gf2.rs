// ============================================================================
// Helper functions (matching Bochs gf2.cc)
// ============================================================================

/// Compute byte parity: returns 1 if val has an even number of 1 bits, 0 otherwise.
/// Matches Bochs scalar_arith.h parity_byte().
#[inline]
fn parity_byte(val: u8) -> u8 {
    ((0x9669u16 >> ((val ^ (val >> 4)) & 0xF)) & 1) as u8
}

/// GF(2) affine transformation of a single byte.
///
/// For each bit i of the result, compute the parity (XOR-reduction) of
/// (src2_qword_byte[7-i] AND src1byte), then XOR with imm8 bit i.
///
/// Matches Bochs affine_byte().
#[inline]
fn affine_byte(src2: u64, src1byte: u8, imm8: u8) -> u8 {
    let src2_bytes = src2.to_le_bytes();
    let mut result: u8 = 0;
    for i in 0..8 {
        // parity_byte returns 1 for even parity; !parity_byte gives 1 for odd parity
        // which is the XOR-reduction of all bits in (src2_byte & src1byte)
        let p = parity_byte(src2_bytes[7 - i] & src1byte);
        // Bochs uses !parity_byte which means: 1 if odd number of bits set
        result |= (p ^ 1) << i;
    }
    result ^ imm8
}

/// GF(2) affine transformation on all 16 bytes of an XMM register.
///
/// Each byte of dst is transformed using the corresponding qword of src
/// as the 8x8 bit matrix. Bytes 0-7 use src.xmm64u[0], bytes 8-15 use src.xmm64u[1].
///
/// Matches Bochs xmm_gf2p8affineqb().
fn xmm_gf2p8affineqb(dst: &mut BxPackedXmmRegister, src: &BxPackedXmmRegister, imm8: u8) {
    unsafe {
        for i in 0..16 {
            dst.xmmubyte[i] = affine_byte(src.xmm64u[i / 8], dst.xmmubyte[i], imm8);
        }
    }
}

/// GF(2) affine transformation with multiplicative inverse on all 16 bytes.
///
/// Same as affine_byte but first applies the GF(2^8) multiplicative inverse
/// to the source byte (using the AES field polynomial).
///
/// Matches Bochs xmm_gf2p8affineinvqb().
fn xmm_gf2p8affineinvqb(dst: &mut BxPackedXmmRegister, src: &BxPackedXmmRegister, imm8: u8) {
    unsafe {
        for i in 0..16 {
            dst.xmmubyte[i] = affine_byte(src.xmm64u[i / 8], GF256_INV[dst.xmmubyte[i] as usize], imm8);
        }
    }
}

/// GF(2^8) multiply two bytes with reduction polynomial 0x11B.
///
/// Uses log/exp table lookup for speed (matches Bochs gf2p8mul fast path).
#[inline]
fn gf2p8mul(a: u8, b: u8) -> u8 {
    if a == 0 || b == 0 {
        return 0;
    }
    let tmp = GF256_LOG[a as usize] as u16 + GF256_LOG[b as usize] as u16;
    let tmp = if tmp > 255 { tmp - 255 } else { tmp };
    GF256_EXP[tmp as usize]
}

// ============================================================================
// Instruction handlers
// ============================================================================

impl<I: BxCpuIdTrait> BxCpuC<'_, I> {
    /// GF2P8AFFINEQB VdqWdqIb -- 66 0F 3A CE
    ///
    /// Galois Field Affine Transformation.
    /// For each byte in dst, compute 8x8 bit-matrix multiply (from src qword)
    /// with the byte, then XOR with immediate byte.
    pub(super) fn gf2p8affineqb_vdq_wdq_ib(&mut self, instr: &Instruction) -> super::Result<()> {
        let mut dst = self.read_xmm_reg(instr.dst());
        let src = self.sse_read_op2_xmm(instr)?;

        xmm_gf2p8affineqb(&mut dst, &src, instr.ib());

        self.write_xmm_reg_lo128(instr.dst(), dst);
        Ok(())
    }

    /// GF2P8AFFINEINVQB VdqWdqIb -- 66 0F 3A CF
    ///
    /// Galois Field Affine Transformation Inverse.
    /// Same as GF2P8AFFINEQB but first applies GF(2^8) multiplicative inverse
    /// to each source byte.
    pub(super) fn gf2p8affineinvqb_vdq_wdq_ib(&mut self, instr: &Instruction) -> super::Result<()> {
        let mut dst = self.read_xmm_reg(instr.dst());
        let src = self.sse_read_op2_xmm(instr)?;

        xmm_gf2p8affineinvqb(&mut dst, &src, instr.ib());

        self.write_xmm_reg_lo128(instr.dst(), dst);
        Ok(())
    }

    /// GF2P8MULB VdqWdq -- 66 0F 38 CF
    ///
    /// Galois Field Multiply Bytes.
    /// Multiplies corresponding bytes in dst and src in GF(2^8) with
    /// the AES reduction polynomial (0x11B).
    pub(super) fn gf2p8mulb_vdq_wdq(&mut self, instr: &Instruction) -> super::Result<()> {
        let mut dst = self.read_xmm_reg(instr.dst());
        let src = self.sse_read_op2_xmm(instr)?;

        unsafe {
            for n in 0..16 {
                dst.xmmubyte[n] = gf2p8mul(dst.xmmubyte[n], src.xmmubyte[n]);
            }
        }

        self.write_xmm_reg_lo128(instr.dst(), dst);
        Ok(())
    }
}

