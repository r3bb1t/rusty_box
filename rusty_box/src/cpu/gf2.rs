#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_parity_byte() {
        // 0x00 has 0 ones (even) -> parity_byte = 1
        assert_eq!(parity_byte(0x00), 1);
        // 0x01 has 1 one (odd) -> parity_byte = 0
        assert_eq!(parity_byte(0x01), 0);
        // 0x03 has 2 ones (even) -> parity_byte = 1
        assert_eq!(parity_byte(0x03), 1);
        // 0xFF has 8 ones (even) -> parity_byte = 1
        assert_eq!(parity_byte(0xFF), 1);
        // 0x7F has 7 ones (odd) -> parity_byte = 0
        assert_eq!(parity_byte(0x7F), 0);
    }

    #[test]
    fn test_gf2p8mul_basic() {
        // 0 * anything = 0
        assert_eq!(gf2p8mul(0, 0x53), 0);
        assert_eq!(gf2p8mul(0x53, 0), 0);
        // 1 * x = x (multiplicative identity)
        assert_eq!(gf2p8mul(1, 0x53), 0x53);
        assert_eq!(gf2p8mul(0x53, 1), 0x53);
        // x * x^{-1} = 1
        for a in 1u16..=255 {
            let a = a as u8;
            let inv = GF256_INV[a as usize];
            assert_eq!(gf2p8mul(a, inv), 1, "a={:#x}, inv={:#x}", a, inv);
        }
    }

    #[test]
    fn test_gf2p8mul_commutativity() {
        // GF multiplication is commutative
        assert_eq!(gf2p8mul(0x53, 0xCA), gf2p8mul(0xCA, 0x53));
        assert_eq!(gf2p8mul(0x02, 0x80), gf2p8mul(0x80, 0x02));
    }

    #[test]
    fn test_affine_identity() {
        // With identity matrix (each row has a single 1 bit) and imm8=0,
        // affine should return the input byte.
        // Identity matrix as u64: byte[7]=0x80, byte[6]=0x40, ..., byte[0]=0x01
        let identity: u64 = 0x0102040810204080;
        for b in 0..=255u8 {
            assert_eq!(affine_byte(identity, b, 0), b, "identity failed for {:#x}", b);
        }
    }

    #[test]
    fn test_affine_xor_imm8() {
        // With identity matrix, result should be src1byte ^ imm8
        let identity: u64 = 0x0102040810204080;
        assert_eq!(affine_byte(identity, 0x00, 0xFF), 0xFF);
        assert_eq!(affine_byte(identity, 0xFF, 0xFF), 0x00);
        assert_eq!(affine_byte(identity, 0xAA, 0x55), 0xFF);
    }

    #[test]
    fn test_gf256_inv_self_consistent() {
        // inv(0) = 0
        assert_eq!(GF256_INV[0], 0);
        // inv(1) = 1
        assert_eq!(GF256_INV[1], 1);
        // inv(inv(x)) = x for all x
        for i in 0..=255u8 {
            assert_eq!(GF256_INV[GF256_INV[i as usize] as usize], i,
                "double-inverse failed for {:#x}", i);
        }
    }
}
